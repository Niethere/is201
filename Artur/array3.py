# Вводим значения N, A и D
N = int(input("Введите количество элементов (N > 1): "))
A = int(input("Введите первый член прогрессии (A): "))
D = int(input("Введите разность прогрессии (D): "))

# Формируем массив арифметической прогрессии
progression = [A + i * D for i in range(N)]

# Выводим массив
print(progression)